"""`retrosync wrap-*` subcommand backends.

The Steam launch shortcuts (generated by Steam ROM Manager, patched by
`deck.srm`) invoke a small bash dispatcher (installed at
`~/.local/bin/retrosync-wrap`) that:

  1. Calls `retrosync wrap-extract-rom` to find the ROM path inside the
     emulator command line.
  2. Calls `retrosync wrap-derive-game-id` to canonicalize ROM-stem →
     `(system, game_id)`.
  3. Calls `retrosync wrap-pre` to do the pre-launch sync + lease grab.
  4. exec's the emulator.
  5. After exit, calls `retrosync wrap-post` to release the lease and
     flush any in-flight uploads.

Splitting into four small Python subcommands keeps the bash dispatcher
trivial (no Python startup-cost during exec) and lets us unit-test each
piece in isolation.

In the spec, "wrap" is the umbrella command operators see; in the code
the verbs are the four wrap-* subcommands the bash script calls.
"""
from __future__ import annotations

